/**
 * Object:
 * Use the `object` type when the variable can hold any non-primitive value.
 */
namespace ss {
  let person: object = { name: "John", age: 30 };
}
/**
 * Custom Objects:
 * Define interfaces or classes to create custom object types.
 */
